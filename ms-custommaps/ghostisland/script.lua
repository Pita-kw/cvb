	if dist > loadDistance then 
		if loaded then 
			streamOutModels()
			loaded = false
		end
	else 
		if not loaded then 
			streamInModels()
			loaded = true
		end
	end
	
	setElementData(localPlayer, "player:medieval", loaded)
end


