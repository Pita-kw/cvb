local function unloadModels()
	for m,n in pairs(pModels) do
		if (isElement(n.col)) then destroyElement(n.col); end
		if (isElement(n.model)) then destroyElement(n.model); end
	end
	
	for m,n in ipairs(objects) do
		local model = getElementModel(n)
		engineRestoreCOL(model)
		engineRestoreModel(model)
	end
	
	pModels = {}
end 

local function streamInModels()
	loadModels();
	
	for m,n in ipairs(objects) do
		source = n
		modelStreamIn();
		setElementDimension(n, 0)
	end
end 

local function streamOutModels()
	unloadModels();
	for m, n in ipairs(objects) do 
		setElementDimension(n, 1213)
	end
end 

